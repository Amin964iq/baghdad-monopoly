interface Props {
  deck: string;
  cardId: number;
  isMyTurn: boolean;
}

export default function CardPopup({ deck, cardId, isMyTurn }: Props) {
  const isChest = deck === 'lucky_chest';
  const cards = isChest ? LUCKY_CHEST : LUCK;
  const text = cards[cardId] || 'بطاقة غير معروفة';

  return (
    <div className="card-popup-overlay">
      <div className={`card-popup ${isChest ? 'chest' : 'luck'}`}>
        <div className="card-type">
          {isChest ? '📦 صندوق اللعبة' : '❓ حظك'}
        </div>
        <div className="card-text">{text}</div>
        {isMyTurn && (
          <button className="action-btn" onClick={() => socket.emit('acknowledge_card')}>
            حسناً ✓
          </button>
        )}
      </div>
    </div>
  );
}
